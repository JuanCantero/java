
public class PersonClass {
	String nom;
	String cognom;
	String edat;
	
	public void setNom(String _nom){
		this.nom = _nom ;
	}
	
	public void setCognom(String _cognom){
		this.cognom = _cognom ;
	}
	
	public void setEdat(String _edat){
		this.edat = _edat;
	}
	
	public String getNom(){
		return nom;
	}
	
	public String getCognom(){
		return cognom;
	}
	
	public String getEdat(){
		return edat;
	}
}
